"""Sensor definitions and register mappings for Weishaupt WTC.

Based on the Weishaupt CanApiJson protocol mapping table and
Modbus TCP register documentation (manual-wem-modbustcp.pdf).

VG frame format: CM(1B) MI(1B) MX(1B) OX(2B) OS(1B) VS(2B) VA(xB)
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import StrEnum

from homeassistant.components.sensor import (
    SensorDeviceClass,
    SensorStateClass,
)
from homeassistant.const import (
    PERCENTAGE,
    UnitOfEnergy,
    UnitOfPower,
    UnitOfPressure,
    UnitOfTemperature,
    UnitOfTime,
)


class WeishauptDeviceGroup(StrEnum):
    """Weishaupt device groups."""

    SG = "sg"  # Systemgerät (System device)
    WTC = "wtc"  # WTC boiler
    HK = "hk"  # Heizkreis (Heating circuit)
    WW = "ww"  # Warmwasser (Domestic hot water) - EM-WW
    SOL = "sol"  # Solar
    KA = "ka"  # Kaskade (Cascade)
    RF = "rf"  # Raumfühler (Room sensor)


@dataclass
class WeishauptSensorDefinition:
    """Definition of a single Weishaupt sensor."""

    key: str  # Unique identifier
    name: str  # Human-readable name
    mi: int  # Module Index
    mx: int  # Member Index / Module Extension
    ox: int  # Object Index
    os: int  # Object Sub-index / Offset
    vs: int  # Value size in bytes
    group: WeishauptDeviceGroup  # Device group
    modbus_reg: str  # Modbus register reference
    device_class: SensorDeviceClass | None = None
    state_class: SensorStateClass | None = None
    unit: str | None = None
    scale: float = 1.0  # Multiply raw value by this to get the actual value
    icon: str | None = None
    value_map: dict[int, str] | None = None  # Map raw values to state strings
    entity_category: str | None = None  # "config" or "diagnostic"
    signed: bool = False  # Whether the value is a signed integer
    poll: bool = True  # Whether this definition should be read from the device
    source_key: str | None = None  # Another sensor key to derive the value from
    byte_offset: int = 0  # Byte offset within the source value_hex for derived sensors
    byte_length: int | None = None  # Number of bytes to read from the source value_hex


